public class Visual {
    private Blade blade;
    private Handle handle;
    private String bloodGroove;

    public Blade getBlade() {
        return blade;
    }

    public void setBlade(Blade blade) {
        this.blade = blade;
    }

    public Handle getHandle() {
        return handle;
    }

    public void setHandle(Handle handle) {
        this.handle = handle;
    }

    public String getBloodGroove() {
        return bloodGroove;
    }

    public void setBloodGroove(String bloodGroove) {
        this.bloodGroove = bloodGroove;
    }
}
